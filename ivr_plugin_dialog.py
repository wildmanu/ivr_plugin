# Interfaz 
FORM_CLASS, _ = uic.loadUiType(os.path.join(
    os.path.dirname(__file__), 'ivr_plugin_dialog_base.ui'
))

class IVRDialog(QtWidgets.QDialog, FORM_CLASS):
    
    def __init__(self, parent=None):
        super().__init__(parent)
        self.setupUi(self)

        uit.setup_ui_texts(self)
        uit.setup_combo_metodos(self)
        uib.setup_ui_logic(self)
        uib.setup_connections(self)
        uib.setup_defaults(self)

    titulos = {
        "entrada": "Seleccionar carpeta de entrada",
        "salida": "Seleccionar carpeta de salida",
    }
        
    def seleccionar_carpeta(self, tipo):
        "Función para la selección y visulización de rutas"
        
        titulo = f"Seleccionar carpeta de {tipo}"
        ruta = QFileDialog.getExistingDirectory(self, titulo, "",
               QFileDialog.ShowDirsOnly)
    
        if not ruta:
            return
    
        if tipo == "entrada":
            self.ruta_entrada = ruta
            if hasattr(self, "visualizar_entrada"):
                self.visualizar_entrada.setText(ruta)
    
        elif tipo == "salida":
            self.ruta_salida = ruta
            if hasattr(self, "visualizar_salida"):
                self.visualizar_salida.setText(ruta)
                
    def verificacion_front(self):
    
        # V1: Ruta de entrada obligatoria
        ruta_entrada = getattr(self, "ruta_entrada", None)
        if not ruta_entrada:
            QMessageBox.warning(
                self,
                "Falta información",
                "No existe ruta de entrada, seleccione una para iniciar el proceso."
            )
            return False
    
        # V2: Si hay método, debe elegirse GAU o LOC
        hay_metodo = hasattr(self, "bt_met") and self.bt_met.currentIndex() != 0
    
        gau = hasattr(self, "bt_ivr_gau") and self.bt_ivr_gau.isChecked()
        loc = hasattr(self, "bt_ivr_loc") and self.bt_ivr_loc.isChecked()
    
        if hay_metodo and not (gau or loc):
            QMessageBox.warning(
                self,
                "Falta información",
                "Ha seleccionado un método de clasificación, pero no el tipo de IVR (GAU o LOC)."
            )
            return False
    
        return True
      
    def ejecutar_proceso(self):
        # 1) Validar inicio
        if not self.verificacion_front():
            return
    
        # 2) Establecer rutas entrada y salida 
        input_path = getattr(self, "ruta_entrada", None)
        output_path = getattr(self, "ruta_salida", None)
        
        # 3) Crear instancia para envío de la tarea al Task Manager
        self.task = TareaProcesamientoIVR(
            
            # Rutas procesos (2)
            input_path = input_path,                     # String 
            output_path = output_path,                   # String 
            # Información capa (4)
            val_org = self.bt_org.isChecked(),
            val_nor = self.bt_nor.isChecked(),
            val_gau = self.bt_gau.isChecked(),
            val_adc = self.br_adc.isChecked(),
            # Información Clasificador GAU (4)
            metodo = self.bt_met.currentText(),         # String 
            clases = self.bt_rag.value(),               # Int 
            ivr_gau = self.bt_ivr_gau.isChecked(),
            ivr_loc = self.bt_ivr_loc.isChecked(),
            # Parámetros Auxiliares (2) 
            val_t15 = self.bt_15.isChecked(),
            val_mix = int(self.bt_mix.opacity() * 100), # Float 
            val_crs = self.bt_crs.currentText(),        # String!
            # Información Auxiliar (4)
            fsig = self.bt_fsig.isChecked(),
            fnor = self.bt_fnor.isChecked(),
            csvmun = self.bt_mun.isChecked(),
            csvpro = self.bt_pro.isChecked(),
            
        )
    
        QgsApplication.taskManager().addTask(self.task)

